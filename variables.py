# What are variables in Python?
# Variables are used to store data in a program. They act as containers for values that can be changed and manipulated throughout the execution of the program.

# Types of Variables in Python:
# 1. Local Variables: These are variables that are defined within a function and can only be accessed within that function.
# 2. Global Variables: These are variables that are defined outside of any function and can be accessed from anywhere in the program.

