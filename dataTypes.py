# What are data types in Python?
# Data types are used to define the type of data that a variable can hold. In Python, there are several built-in data types that can be used to store different kinds of data.
#  Built-in Data Types in Python:

# Text Type     :	    str
# Numeric Types :	    int, float, complex
# Sequence Types:	    list, tuple, range
# Mapping Type  :	    dict
# Set Types     :	    set, frozenset
# Boolean Type  :	    bool
# Binary Types  :	    bytes, bytearray, memoryview
# None Type     :	    NoneType


a = 5
b = 3.14
c = 2 + 3j
d = "Hello, World!"
e = [1, 2, 3, 4, 5] 
f = (1, 2, 3, 4, 5)

print(a)
print(b)
print(c)
print(d)
print(e)
print(f)
print("========================================= ")
print(type(a))  # Output: <class 'int'>
print(type(b))  # Output: <class 'float'>   
print(type(c))  # Output: <class 'complex'>
print(type(d))  # Output: <class 'str'>
print(type(e))  # Output: <class 'list'>
print(type(f))  # Output: <class 'tuple'>